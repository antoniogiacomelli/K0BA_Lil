void init_ITM(void) {
#ifdef ITM_ON

	   // Enable trace
	    CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk;

	    // Unlock ITM
	    ITM->LAR = 0xC5ACCE55;

	    // Enable ITM and timestamping
	    ITM->TCR = ITM_TCR_ITMENA_Msk    // Enable ITM
	               | ITM_TCR_TSENA_Msk   // Enable timestamping
	               | ITM_TCR_SYNCENA_Msk; // Enable synchronization

	    // Enable ITM Port 0
	    ITM->TER |= (1UL << 0);

	    // Configure DWT for cycle counter
	    DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk;  // Enable cycle counting
#endif
    return;
}

